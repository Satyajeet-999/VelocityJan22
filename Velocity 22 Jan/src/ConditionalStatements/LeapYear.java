package ConditionalStatements;

public class LeapYear {
	public static void main(String[] args) {
		//Leap Year
		int Days=366;
		if (Days==366) {
			System.out.println("The Year Is a Leap Year ");
		}else {
			System.out.println("The Year is not a Leap Year");
		}
		//Not a Leap Year
		int D=365;
		if (D==366) {
			System.out.println("The Year Is a Leap Year ");
		}else {
			System.out.println("The Year is not a Leap Year");
		}
		
	}

}
