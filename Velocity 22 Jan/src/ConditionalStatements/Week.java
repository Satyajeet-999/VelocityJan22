package ConditionalStatements;

public class Week {
	public static void main(String[] args) {
		int Date=22;
		if (Date<=7) {
			System.out.println("First Week");
		}else if (Date<=14) {
			System.out.println("Second Week");
		}else if (Date<=21) {
			System.out.println("Third Week");
		}else  {
			System.out.println("Fourth Week");
		}
	}

}
